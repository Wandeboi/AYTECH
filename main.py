# Check if two words are anagrams 
# Example:
# find_anagrams("hello", "check") --> False
# find_anagrams("below", "elbow") --> True


s1 ='listen'
s2 ='silent'

if(sorted(s1) == sorted(s2)):
        print ('True, the words are Anagram')
else:
        print('False, the words are not Anagram')

        

